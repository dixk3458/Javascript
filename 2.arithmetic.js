// 산술 연산자 (Arithmetic operators)
// + 더하기
// - 빼기
// * 곱하기
// / 나누기
// % 나머지
// ** 지수 연산자

console.log(5 + 2);
console.log(5 / 2);
console.log(5 % 2);
console.log(5 ** 2);
console.log(5 ** 3);
console.log(Math.pow(5,3));

